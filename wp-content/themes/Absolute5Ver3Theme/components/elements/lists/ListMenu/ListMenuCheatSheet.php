<?php
/*
::::::::::::::::::::::::::::::::::::::::::::::::::::::::

ListMenu の Props 一覧
(components/elements/lists/ListMenu/ListMenu.php)

グローバルナビゲーション・ドロワーメニュー用の
リストアイテム群を出力します。
<ul> は呼び出し元で用意します。
このコンポーネントは <li> 要素だけを出力します。

固定メニュー:
- Home（トップ / #home）
- About（/about）
- News（/news）

オプションメニュー:
- Contact（/contact）→ contact => true 時
- Privacy Policy（/privacy-policy）→ pp => true 時

--------------------------------

contact?: bool
Contact メニュー項目を表示します。

例:
- 'contact' => true

--------------------------------

pp?: bool
Privacy Policy メニュー項目を表示します。

例:
- 'pp' => true

--------------------------------

ホームリンクの特殊動作

- is_home() または is_front_page() の場合 → href="#home"
- それ以外のページ → href=home_url('/')

::::::::::::::::::::::::::::::::::::::::::::
*/
?>

<!-- ::::::::::::::::::::::::::::::::::::::::::::::::::::::
  基本：固定メニューのみ（Home / About / News）
::::::::::::::::::::::::::::::::::::::::::::::::::::::: -->
<ul>
  <?php
  C_Elements('ListMenu', []);
  ?>
</ul>

<!-- ::::::::::::::::::::::::::::::::::::::::::::::::::::::
  Contact あり
::::::::::::::::::::::::::::::::::::::::::::::::::::::: -->
<ul>
  <?php
  C_Elements('ListMenu', [
    'contact' => true,
  ]);
  ?>
</ul>

<!-- ::::::::::::::::::::::::::::::::::::::::::::::::::::::
  Contact + Privacy Policy あり
::::::::::::::::::::::::::::::::::::::::::::::::::::::: -->
<ul>
  <?php
  C_Elements('ListMenu', [
    'contact' => true,
    'pp'      => true,
  ]);
  ?>
</ul>

<!-- ::::::::::::::::::::::::::::::::::::::::::::::::::::::
  ナビゲーション内での典型的な使い方
::::::::::::::::::::::::::::::::::::::::::::::::::::::: -->
<nav>
  <ul class="nav-list">
    <?php
    C_Elements('ListMenu', [
      'contact' => true,
    ]);
    ?>
  </ul>
</nav>
